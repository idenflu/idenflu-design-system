const fs = require("fs");
const path = require("path");

const rootDir = path.resolve(__dirname, "..");
const tokensPath = path.join(rootDir, "tokens.json");
const outputPath = path.join(rootDir, "tokens.generated.css");

const tokens = JSON.parse(fs.readFileSync(tokensPath, "utf8"));

const toCssVar = (name) => `--${name}`;
const entries = new Map();

const add = (name, value) => {
  if (value !== undefined && value !== null) entries.set(toCssVar(name), String(value));
};

Object.entries(tokens.colors || {}).forEach(([name, token]) => add(name, token.value));
Object.entries(tokens.spacing || {}).forEach(([name, value]) => add(`space-${name}`, value));
Object.entries(tokens.sizing || {}).forEach(([name, value]) => add(name, value));
Object.entries(tokens.radius || {}).forEach(([name, value]) => add(`radius-${name}`, value));
Object.entries(tokens.shadow || {}).forEach(([name, value]) => add(`shadow-${name}`, value));
Object.entries(tokens.motion || {}).forEach(([name, value]) => add(name, value));
Object.entries(tokens.layering || {}).forEach(([name, value]) => add(`z-${name}`, value));
Object.entries(tokens.breakpoints || {}).forEach(([name, value]) => add(`breakpoint-${name}`, value));

const lightTheme = Object.entries(tokens.themes?.light || {}).map(([name, value]) => [toCssVar(name === "chrome" ? "chrome-bg" : name), value]);
const darkTheme = Object.entries(tokens.themes?.dark || {}).map(([name, value]) => [toCssVar(name === "chrome" ? "chrome-bg" : name), value]);

const renderBlock = (selector, variables) => {
  const body = variables.map(([name, value]) => `  ${name}: ${value};`).join("\n");
  return `${selector} {\n${body}\n}`;
};

lightTheme.forEach(([name, value]) => entries.set(name, String(value)));

const css = [
  "/* Generated by scripts/build-tokens.js from tokens.json. */",
  renderBlock(":root", [...entries.entries()]),
  renderBlock(':root[data-theme="dark"]', darkTheme),
  "",
].join("\n\n");

fs.writeFileSync(outputPath, css, "utf8");
console.log("built tokens.generated.css");
